namespace Git_Tutorials
{
    public class Calculator
    {
        //This adds two numbers
        public static int AddTwoNumbers(int num1, int num2)
        {
            return num1 + num2;
        }

        //This subtract two numbers
        public static int SubtractTwoNumbers(int num1, int num2)
        {
            return num1 - num2;
        }
    }
